"""Анализ диалогов по ключевым словам из сообщений КЛИЕНТОВ.

Считаем не сырую частоту слов, а попадание диалога в тематические категории по
словарям (поводы, получатель, цветы, способ получения, бюджет). Единица счёта —
ДИАЛОГ (клиент): категория засчитывается, если хоть одно сообщение клиента в этом
диалоге совпало с ключевым словом. Совпадение — по основе слова на границе слова
(\bроз ловит «розы/розами», но не «мороз»). Это эвристика, а не точная разметка.
"""
from __future__ import annotations

import re

# Список групп: (Название группы, [(Подпись, [основы-паттерны]) ...]).
# Паттерны — «сырые» куски regex; матчим по границе слова, регистронезависимо, ё→е.
_CATEGORIES: list[tuple[str, list[tuple[str, list[str]]]]] = [
    ("Поводы", [
        ("День рождения", [r"день рожд", r"дн[ею] рожд", r"\bдр\b", r"днюх", r"имениннц?", r"\bдни рожд"]),
        ("Юбилей", [r"юбиле", r"юбилян"]),
        ("8 марта", [r"8\s*март", r"восьм\w* март", r"женск\w* (день|праздник)"]),
        ("14 февраля", [r"14\s*феврал", r"валентин", r"день влюбл"]),
        ("23 февраля", [r"23\s*феврал", r"защитник"]),
        ("Новый год", [r"новогодн", r"нов\w* год"]),
        ("Свадьба", [r"свадьб", r"невест", r"жених", r"бракосоч"]),
        ("Годовщина", [r"годовщин", r"лет вместе"]),
        ("Рождение ребёнка", [r"роддом", r"выписк", r"новорожд", r"родила", r"рожени"]),
        ("Извинения", [r"извин", r"\bпрост(и|ите|ить)", r"помирит", r"загладить"]),
        ("Без повода", [r"без повод", r"просто так", r"без причин", r"порадовать"]),
    ]),
    ("Кому букет", [
        ("Маме", [r"\bмам"]),
        ("Жене", [r"\bжен[еуа]\b", r"супруг"]),
        ("Девушке / любимой", [r"девушк", r"любим", r"возлюбл"]),
        ("Подруге", [r"подруг"]),
        ("Коллеге / начальнице", [r"коллег", r"начальниц", r"руководител", r"сотрудниц"]),
        ("Бабушке", [r"бабушк", r"бабул"]),
        ("Дочери", [r"дочк", r"дочер", r"доченьк"]),
    ]),
    ("Цветы", [
        ("Розы", [r"\bроз"]),
        ("Пионы", [r"пион"]),
        ("Тюльпаны", [r"тюльпан"]),
        ("Хризантемы", [r"хризантем"]),
        ("Герберы", [r"гербер"]),
        ("Лилии", [r"лили"]),
        ("Гортензии", [r"гортенз"]),
        ("Ромашки", [r"ромашк"]),
        ("Орхидеи", [r"орхиде"]),
        ("Эустомы", [r"эустом", r"лизиантус"]),
        ("Альстромерии", [r"альстр[оё]мер"]),
        ("Гвоздики", [r"гвоздик"]),
        ("Подсолнухи", [r"подсолн"]),
    ]),
    ("Способ получения", [
        ("Доставка", [r"достав", r"привез", r"привоз", r"курьер"]),
        ("Самовывоз", [r"самовывоз", r"самовыв", r"\bзаберу", r"\bзабрать", r"\bзаеду", r"\bподъед", r"из магазин"]),
    ]),
    ("Бюджет", [
        ("Подешевле", [r"подешевл", r"дешев", r"бюджетн", r"недорог", r"попроще", r"эконом", r"не дорого"]),
        ("Подороже / премиум", [r"подорож", r"дорог(о|ой|ая)", r"премиум", r"роскош", r"солидн", r"побольше", r"покрупне", r"\bвип\b", r"элитн"]),
    ]),
]


def _compile(items: list[tuple[str, list[str]]]) -> list[tuple[str, re.Pattern]]:
    out = []
    for label, pats in items:
        rx = re.compile(r"\b(?:" + "|".join(pats) + r")", re.IGNORECASE)
        out.append((label, rx))
    return out


# Прекомпилируем один раз при импорте.
_COMPILED: list[tuple[str, list[tuple[str, re.Pattern]]]] = [
    (group, _compile(items)) for group, items in _CATEGORIES
]


def _norm(text: str) -> str:
    return text.lower().replace("ё", "е")


def analyze(dialog_texts: list[str]) -> dict:
    """dialog_texts — по одной строке на диалог (склеенные сообщения клиента).
    Возвращает {"total": N, "groups": [{"name", "items": [{"label", "count"}]}]}."""
    normalized = [_norm(t) for t in dialog_texts if t]
    total = len(normalized)
    groups = []
    for group, items in _COMPILED:
        rendered = []
        for label, rx in items:
            count = sum(1 for t in normalized if rx.search(t))
            rendered.append({"label": label, "count": count})
        rendered.sort(key=lambda x: x["count"], reverse=True)
        groups.append({"name": group, "items": rendered})
    return {"total": total, "groups": groups}
